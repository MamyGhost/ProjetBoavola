public class Matiere {
    private int id;
    private String nom;
    private int coeeficient;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getCoeeficient() {
        return coeeficient;
    }

    public void setCoeeficient(int coeeficient) {
        this.coeeficient = coeeficient;
    }
}
